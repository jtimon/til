#pragma once
#include "til_decls.h"

#include "til_vec.h"
#include "til_str.h"

typedef struct Dynamic {
    char _;
} Dynamic;


struct FuncType {
    FuncType_tag tag;
};






typedef struct Range {
    U64 start;
    U64 end;
} Range;



typedef struct FunctionDef {
    FuncType func_type;
    Vec param_names;
    Vec param_types;
    Vec param_muts;
    Vec param_owns;
    Vec param_shallows;
    Vec param_fn_sigs;
    U32 nparam;
    Vec param_defaults;
    Str return_type;
    I32 variadic_index;
    I32 kwargs_index;
    Bool return_is_ref;
    Bool return_is_shallow;
} FunctionDef;


Bool * FuncType_eq(FuncType * self, FuncType * other);
